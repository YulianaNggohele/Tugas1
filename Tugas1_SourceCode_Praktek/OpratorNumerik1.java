public class OpratorNumerik1 {

	public static void main(String[] args) {
		float x, y, res;
		
		x = 5 ;
		y = 5 ;
		
		res = x + y; 
		System.out.println("x + y = "+ res);

		res = x - y; 
		System.out.println("x - y = "+ res);
 
		res = x / y; 
		System.out.println("x / y= "+ res);
		
		res = x * y;
		System.out.println("x * y = "+ res);
	}
}