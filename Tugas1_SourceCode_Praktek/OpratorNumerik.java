public class OpratorNumerik {

	public static void main(String[] args) {
		int i, j, hsl ; 
		
		i = 5;
		j = 2;
		
		hsl = i + j; ;
		System.out.println("i + j = "+ hsl);

		hsl = i / j; 
		System.out.println("i / j = "+ hsl);
 
		hsl = i * j;
		System.out.println("i * j ="+ hsl);
		
		hsl = i /j ;
		System.out.println("i /j = "+ hsl);
		
		hsl = i%j ;
		System.out.println("i%j = "+ hsl);
	}
}